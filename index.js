// Function expression called 'divide'
const divide = function () {
  return 2000 / 100;
};

// Arrow function called 'square'
const square = (num) => {
  return num * num;
};

// Arrow function called 'add'
const add = (num1, num2) => {
  return num1 + num2;
};

// Test the functions
console.log(divide()); 
console.log(square(5)); 
console.log(add(3, 7)); 
